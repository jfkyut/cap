<script setup>
import { onMounted, ref, watch } from 'vue';
import ApexCharts from 'apexcharts';
import { useUsageService } from '@/services/usageService';

const { getUsageReportRequest } = useUsageService()

const showDropdown = ref(false);

const toggleDropdown = () => {
  (showDropdown.value)
    ? showDropdown.value = false
    : showDropdown.value = true
}

const days = ref(null);
const chartOptions = ref(null);
const chart = ref(null);

const dates = ref({
  start: null,
  end: null
})

const updateOptions = (counts, names) => {
  chartOptions.value = {
    series: counts,
    chart: {
      height: 420,
      width: "100%",
      type: "pie",
    },
    stroke: {
      colors: ["white"],
      lineCap: "",
    },
    plotOptions: {
      pie: {
        labels: {
          show: true,
        },
        size: "100%",
        dataLabels: {
          offset: -25
        }
      },
    },
    labels: names,
    dataLabels: {
      enabled: true,
      style: {
        fontFamily: "Inter, sans-serif",
      },
    },
    legend: {
      position: "bottom",
      fontFamily: "Inter, sans-serif",
      labels: {
        colors: ["#aaa", "#aaa"],
      }
    },
    yaxis: {
      labels: {
        formatter: function (value) {
          return value + " minutes"
        },
      },
    },
    xaxis: {
      labels: {
        formatter: function (value) {
          return value  + "%"
        },
      },
      axisTicks: {
        show: false,
      },
      axisBorder: {
        show: false,
      },
    },
  }
}

onMounted( async () => {

  days.value = parseInt(sessionStorage.getItem('pie-usage-last-days')) || 7;

  const { data } = await getUsageReportRequest({ days: days.value });

  dates.value.start = data.start_date;
  dates.value.end = data.end_date

  updateOptions([
    data.chatbot,
    data.travel
  ], Object.keys(data).slice(0, 2));

  if (document.getElementById("pie-chart") && typeof ApexCharts !== 'undefined') {
    chart.value = new ApexCharts(document.getElementById("pie-chart"), chartOptions.value);
    chart.value.render();
  }
})

watch(days, async (days) => {
  sessionStorage.setItem('pie-usage-last-days', days);

  const { data } = await getUsageReportRequest({ days: days });

  dates.value.start = data.start_date;
  dates.value.end = data.end_date

  updateOptions([
    data.chatbot,
    data.travel
  ], Object.keys(data).slice(0, 2));

  chart.value.updateOptions(chartOptions.value);
})

</script>

